    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        String firstName  = FirstNameTextField.getText();
        String middleName = MiddleNameTextField.getText();
        String lastName   = LastNameTextField.getText();
        String address    = AddressTextField.getText();
        String user_name  = UserNameTextField.getText();
        String password   = PasswordTextField.getText();
        boolean is_buyer   = buyerRadioButton.isSelected();
        boolean is_seller  = sellerRadioButton.isSelected();

        if(firstName.isEmpty() ||
            lastName.isEmpty() ||
            address.isEmpty() ||
            user_name.isEmpty() ||
            password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please ensure that all fields all filled.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            userRegister(firstName, middleName, lastName, address, user_name, password, is_buyer, is_seller);
        }
    }//GEN-LAST:event_registerButtonActionPerformed

