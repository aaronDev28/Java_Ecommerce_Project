    private void userRegister(String firstName, String middleName, String lastName, String address, String user_name, String password, boolean is_buyer, boolean is_seller) {
        Connection dbconn = (new DBConnection()).connectDB();

        try {
                PreparedStatement st = dbconn.prepareStatement("SELECT * FROM users WHERE username = ?");
            
                st.setString(1, user_name);
                
                ResultSet res = st.executeQuery();
                
                if(res.next()) {
                    JOptionPane.showMessageDialog(this, "Username is already in use. Please choose another.", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    PreparedStatement st2 = dbconn.prepareStatement("INSERT INTO users (firstname, middlename, lastname, address, username, password, accesstype) VALUES (?, ?, ?, ?, ?, ?, ?)");
            
                    st2.setString(1, firstName);
                    st2.setString(2, middleName);
                    st2.setString(3, lastName);
                    st2.setString(4, address);
                    st2.setString(5, user_name);
                    st2.setString(6, password);            
            
                    int accessType = 1;
                    if(is_seller) { accessType = 2; }

                    st2.setInt(7, accessType);   

                    int res2 = st2.executeUpdate();

                    JOptionPane.showMessageDialog(this, "User account created.", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    
                    FirstNameTextField.setText(null);
                    MiddleNameTextField.setText(null);
                    LastNameTextField.setText(null);
                    AddressTextField.setText(null);
                    UserNameTextField.setText(null);
                    PasswordTextField.setText(null);
                    
                    st2.close();
                }
                
                res.close();
                st.close();
        } catch (SQLException ex) {
            Logger.getLogger(LoginScreen.class.getName()).log(Level.SEVERE, null, ex);
       }
    }
